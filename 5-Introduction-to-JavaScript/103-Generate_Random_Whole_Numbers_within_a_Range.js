/* The same method can be used to create random numbers within a specified
range, rather than starting at 0. Multiply by the value of the maximum less
the minimum plus 1, and then add the minimum. */

// Example
function ourRandomRange(ourMin, ourMax) {

  return Math.floor(Math.random() * (ourMax - ourMin + 1)) + ourMin;
}

ourRandomRange(1, 9);

